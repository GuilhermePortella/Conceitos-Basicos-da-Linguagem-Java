package Algoritmos;

public class Pag_137_Al_181 {
    
    public static void main(String[] args) {
        
        int soma = 0;
        
        for (int j = 1; j <= 100; j++) {
            soma += j;
            System.out.println(j+ " ");
        }
        System.out.println("Somatorio de 1 a 100: " + soma);
    }
}
