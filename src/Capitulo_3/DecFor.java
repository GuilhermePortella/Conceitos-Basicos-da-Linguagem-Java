public class DecFor {
    
    //laço for sendo execultado em sentido negativo
    
    public static void main(String[] args) {
        
        int x;
        
        
        for(x = 100; x> -100; x -=5){ //A variavel de controle de laço e sempre decrementada em 5 unudades
            System.out.println(x);
        
        }
    }
}
